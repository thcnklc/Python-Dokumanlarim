#Örnek modülde işlemler:
from math import floor , ceil
#"math" modülümüzden sadece "floor" ve "ceil" fonksiyonlarını aldık.
print(floor(5.2))
#"math" modülümüzün içindeki "floor" fonksiyonumuzu kullandık.
print(ceil(5.2))
#"math" modülümüzün içindeki "ceil" fonksiyonumuzu kullandık.
